					9. La Marca se reserva el derecho de corroborar la elegibilidad de los participantes y la validez de las
					participaciones y rechazarlas si no cumplen con estas Reglas. Todo(a) participante deberá cumplir
					fielmente con estas Reglas para optar a ganar y recibir premio. La Marca o personas designadas por La
					Marca, serán los máximos interpretadores de este Reglamento.

				</p>

