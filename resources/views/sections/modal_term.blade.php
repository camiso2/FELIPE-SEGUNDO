				<br>
				<p>1. <u><b>Tiempo de Duración:</b></u> La promoción comenzará el lunes 28 de diciembre de 2020 y finalizará el lunes 4 de enero de 2021. <u>Nada que comprar para participar, ni será necesario realizar una prestación o contraprestación para participar de la promoción o sorteo.</u>
</p>
				

				<p>
					2. El concurso es promovido por Felipe II, por conducto de su distribuidora Ballester Hermanos y su
					agencia de promociones High Impact (en adelante, en colectivo podrán ser referidos como “La Marca”).
					La Marca proveerá información del concurso en los Supermercados ECONO y en la página web del
					concurso <a href="https://www.ganaconfelipesegundo.com">www.ganaconfelipesegundo.com</a>. Las personas pueden ir a la góndola (<i>Point of Sale</i> (POS))
					donde se vende Felipe II en los Supermercados ECONO y “scan” el código (QR Code) del concurso lo
					cual les llevará a la página web www.ganaconfelipesegundo.com, y allí encontrarán información
					detallada para participar. 
				</p>

				<p>
    3. <u><b>Promoción:</b></u> Habrá dos maneras de participar. La persona puede ir a la góndola (<i>Point of Sale</i> (POS))
					donde se vende Felipe II y “scan” el código (QR Code) del concurso escanea el código los cual le
					proveerá información detallada del concurso llevándolo a la página web
					<a href="https://www.ganaconfelipesegundo.com">www.ganaconfelipesegundo.com</a>. La persona interesada en participar puede seleccionar el producto
					Felipe II en la góndola (POS) e ir a la caja registradora a comprar el mismo. Una vez lo compre, le
					tomará una foto al recibo y visitará la página web del concurso (<a href="https://www.ganaconfelipesegundo.com">www.ganaconfelipesegundo.com</a>) para
					registrar su participación, llenando el formulario correspondiente, incluyendo su información de contacto
					y cualquier otra información que se le solicite.
					<br><br>
					Toda persona participante deberá asegurarse de incluir toda la información solicitada en el formulario,
					incluyendo su nombre y apellidos, fecha de nacimiento, número de teléfono, correo electrónico e
					identificar el supermercado ECONO en el cual hizo la compra.
					<br><br>
					También se podrá <u>participar de manera alternativa sin nada que comprar</u>, enviando un correo electrónico
					a la representante de La Marca, Emelia Whitaker a <a href="mailto:ewhitaker@bhipr.com">ewhitaker@bhipr.com</a>, incluyendo el nombre,
					apellidos, número de teléfono, correo electrónico (email) y fecha de nacimiento de la persona
					participante, junto con la frase “La mejor sangría es con Felipe II”.
				</p>

				<p>
					4. <b><u>Fecha de las categorías y Selección de participaciones ganadoras:</u> </b>El día 5 de enero de 2021 se
					escogerán las participaciones ganadoras mediante método aleatorio digital. Se seleccionarán <b>treinta y
    tres (33) participaciones ganadoras</b> en total, a saber:<br>
					<b>PRIMER LUGAR:</b> Tres (3) participaciones ganadoras de primer lugar.<br>
					<b>SEGUNDO LUGAR:</b> Diez (10) participaciones ganadoras de segundo lugar.<br>
                        <b>TERCER LUGAR:</b> Veinte (20) participaciones ganadoras de tercer lugar.<br>
    Se seleccionarán además <b>cinco (5) participaciones ganadoras alternas</b>, que se podrían convertir en
					participaciones ganadoras para primero, segundo o tercer lugar, en caso de que alguna de las
					participaciones o personas ganadoras de conformidad con estas Reglas resulten no elegibles; sean
					descalificadas; o, no reclamen su premio oportunamente.

				</p>

				<p>
    5. <u><b>Premios:</b></u> Los premios serán los siguientes:<br>
					<b>PRIMER LUGAR:</b> Tres (3) Parrandas Virtuales con Ale Croatto<br>
					<b>SEGUNDO LUGAR:</b> Diez (10) tarjetas de regalo (“Gift Cards”) para compras en ECONO valoradas
					en <b>Trecientos Dólares $300.00</b>.<br>
    <b>TERCER LUGAR:</b> Veinte (20) tarjetas de regalo (“Gift Cards”) para compras en ECONO valoradas
					en <b>Ciento Cincuenta Dólares ($150.00)</b>.
					<br><br>
					Se seleccionará un total de <b>treinta y tres (33) participaciones ganadoras</b>, cuyo valor en total suma más
					de $8,000.00 en premios.
					<br><br>
					Toda persona que resulte ganadora del concurso y reclame su premio deberá cumplir con los requisitos
					de elegibilidad y mostrar identificación con foto y fecha de nacimiento. De lo contrario no podrá recibir
					el premio.
					<br><br>
					Luego de reclamado el premio, la persona ganadora tendrá <b>treinta (30) días para recogerlo</b>. En el caso
					del <b>PRIMER LUGAR</b>, la “Parranda Virtual con Ale Croatto”, las tres (3) personas ganadoras tendrán
					hasta el <b>12 de enero de 2021</b>, para coordinar la fecha de la parranda virtual la cual <b> tendrá que coordinar
					para el mes de enero de 2021 (en o antes del 31 de enero de 2021)</b>. Las personas ganadoras que no
					cumplan con esas fechas o términos perderán el derecho a reclamar el premio y los premios no serán
					sustituidos por metálico u otro premio del mismo valor.


				</p>
				<p>
					6. La Marca estará proveyendo información y detalles de la promoción y concurso a través de los
					Supermercados participantes y de la página web <a href="https://www.ganaconfelipesegundo.com">www.ganaconfelipesegundo.com</a>. La Marca tendrá
					disponibles copias de estas Reglas en la referida página “web” y en cada Supermercado participante para
					revisión y toda persona podrá solicitar una copia de las Reglas en el “counter” de servicio al cliente.
				</p>
				<p>
					7. <b><u>Elegibilidad:</u></b> Toda persona que llene un boleto de participación:
<br>        • Deberá ser persona natural y residente <i>bona fide</i> de Puerto Rico; y,
<br>        • Deberá haber ya cumplido al menos dieciocho (18) años al momento de llenar su participación.
<br>        • Deberá mostrar una identificación con foto que incluya su fecha de nacimiento, para reclamar su premio.
<br>        • No podrá estar entre las personas o grupos excluidos de participación en estas Reglas.
					</p>

					
					
					

				<p></p>

				<p>
					8. <b><u>Elección de Participaciones Ganadoras y Ganadoras Alternas</u></b>:<br>
					Las participaciones ganadoras y participaciones ganadoras alternas serán seleccionadas el día <u>5 de
    enero de 2020</u>. Los sorteos se llevarán a cabo de manera digital por la persona designada por La
					Marca.
					<br><br>
					Luego de sacar todas las participaciones ganadoras mediante sorteo, La Marca, se comunicará con
					cada persona ganadora a través del número de teléfono informado en el boleto de participación,
    <u>mediante llamada o texto</u>. Si no se recibe contestación a la llamada o al texto que se envíe, reclamando
					su premio dentro del periodo de <b>setenta y dos (72) horas</b> desde que se realizó llamada, se dejó
					mensaje de voz o se le envió texto, se procederá a descalificar la participación ganadora y se llamará
					a la primera persona con la participación ganadora alterna, a quien se le contactará por el mismo
					mecanismo y dispondrá del mismo término o periodo de tiempo para responder y reclamar su premio.
					Una vez comunicado y aceptado el premio, La Marca publicará la lista de las personas ganadoras en
					la página web <a href="https://www.ganaconfelipesegundo.com">www.ganaconfelipesegundo.com</a>.
					<br><br>
    Los premios <u>no</u> son transferibles. Tampoco se sustituirá el premio por otro de menor, igual o mayor
					valor, ni será el premio redimible en todo o en parte por dinero en efectivo u otras consideraciones.
					
				</p>

