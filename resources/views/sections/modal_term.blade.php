    el cuarto grado de consanguinidad y segundo grado de afinidad</u>, así como <u>personas que convivan</u> o tengan
					relaciones de pareja, aunque no convivan, con empleados(as) de La Marca, o de sus agencias de
					publicidad, relaciones públicas, medios, mercadeo directo, digital y promociones.
				</p>

