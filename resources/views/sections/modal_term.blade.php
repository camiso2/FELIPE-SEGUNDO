					16. Cualquier impuesto o arbitrio que grave el premio a la fecha de entrega a las personas ganadoras,
					será responsabilidad exclusiva de las personas ganadoras. Igualmente, de mediar un impuesto o arbitrio
					con posterioridad a la adjudicación del premio, la persona ganadora asumirá cualquier responsabilidad
					en cuanto al mismo.
